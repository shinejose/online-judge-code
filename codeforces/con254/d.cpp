const int MAXN = 100005;
ll a[MAXN];
ll b[MAXN];
ll n,d,x;
void getNextX() {
    x = (x * 37 + 10007) % 1000000007;
    return x;
}
void  initAB() {
    for(i = 0; i < n; i = i + 1){
        a[i] = i + 1;
    }
    for(i = 0; i < n; i = i + 1){
        swap(a[i], a[getNextX() % (i + 1)]);
    }
    for(i = 0; i < n; i = i + 1){
        if (i < d)
            b[i] = 1;
        else
            b[i] = 0;
    }
    for(i = 0; i < n; i = i + 1){
        swap(b[i], b[getNextX() % (i + 1)]);
    }
}
int main(){
    ios::sync_with_stdio(false);
    cin >> n >>d >>x;
    initAB();
    
    return 0;
}









